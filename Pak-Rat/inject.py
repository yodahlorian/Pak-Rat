"""
Pak Rat v3 — "Add Asset" injection engine.

Adds genuinely NEW content to Retro Rewind (not a swap). The catalogue lists
purchasable/placeable items via Blueprint functions
`Return Catalogue <Category> product class` inside UI_Catalogue_Widget. Editing
that widget's compiled bytecode by hand is a dead end (a correct edit is a full
Kismet recompile, not a byte insert). So v3 registers new items at RUNTIME with
a tiny UE4SS Lua mod that hooks those functions and appends the new class to the
returned array — proven in-game, no widget edit, no crash.

Pipeline:
  1. Cook the user's model into a Decoration-derived Blueprint at a NEW /Game
     path (reuses the v2 UE cooker) so it is a real, loadable class.
  2. Pack the cooked asset into a loose `_P.pak`.
  3. Generate a UE4SS Lua mod that LoadAsset()s each new class and appends it to
     its catalogue category at runtime, plus an INI manifest tracking everything
     added (so re-runs are idempotent / additive).

Requires UE4SS installed in the game (for the runtime hook) AND Unreal Engine
(for the cook). The GUI gates the mode on `ue4ss_available()`.
"""
