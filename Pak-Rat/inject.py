MOD_NAME = "PakRatInjector"

# Content types shown on the v3 "what are you adding?" step. Each maps to a
# catalogue product-class function (`category`). Only types with mapped=True are
# selectable in the GUI; the rest are placeholders until their catalogue path is
# reverse-engineered and proven. Order = display order.
# Shelves(Container) + Equipment(Equipmement) use the SAME proven catalogue hook
# as Decoration — `Return Catalogue <category> product class` on UI_Catalogue_Widget,
# which the generated Lua already generalizes over. The cook yields a Decoration-
# derived BP; `category` just routes which catalogue tab it's appended to.
# Snacks/Drinks/Toys have NO hookable function (their product list is a static data
# array on a stock-box CDO — ToysBox/DrinkBox/ConcessionsShelf), so they stay gated.
CONTENT_TYPES = [
    {"key": "Shelves",    "category": "Container",   "mapped": True},
    {"key": "Decoration", "category": "Decoration",  "mapped": True},
    {"key": "Equipment",  "category": "Equipmement", "mapped": True},
    {"key": "Snacks",     "category": None,          "mapped": False},
    {"key": "Drinks",     "category": None,          "mapped": False},
    {"key": "Toys",       "category": None,          "mapped": False},
]


def mapped_content_types() -> list[dict]:
    return [t for t in CONTENT_TYPES if t["mapped"]]


def content_type(key: str) -> dict | None:
    for t in CONTENT_TYPES:
        if t["key"] == key:
            return t
    return None


# ---------------------------------------------------------------------------
# UE4SS discovery (mirrors cook.ue_available for the GUI gate)
# ---------------------------------------------------------------------------
def _game_binaries_dir() -> Path | None:
    """<...>/RetroRewind/RetroRewind/Binaries/Win64 — sibling of Content/Paks."""
    try:
        paks = core._rr_paks_dir()          # <...>/RetroRewind/Content/Paks
    except Exception:
        return None
    win64 = paks.parent.parent / "Binaries" / "Win64"
    return win64 if win64.is_dir() else None


def ue4ss_dir() -> Path | None:
    win64 = _game_binaries_dir()
    if not win64:
        return None
    d = win64 / "ue4ss"
    if (d / "UE4SS.dll").is_file() or (d / "Mods").is_dir():
        return d
    return None


def ue4ss_mods_dir() -> Path | None:
    d = ue4ss_dir()
    return (d / "Mods") if d else None


def ue4ss_available() -> bool:
    return ue4ss_dir() is not None


# ---------------------------------------------------------------------------
# Lua injector + INI manifest generation
# ---------------------------------------------------------------------------
_LUA_HEADER = """-- ============================================================
-- PakRat Injector  (auto-generated by Pak Rat v3 — do not hand-edit)
-- Registers added items into the Retro Rewind catalogue at runtime by
-- hooking the per-category product-class functions and appending the new
-- classes to the returned array. Proven mechanism; no widget bytecode edit.
-- Regenerate by re-running Pak Rat "Add Asset".
-- ============================================================
local WIDGET_CLASS = "%(widget)s"

-- items[category] = { { pkg=<package path>, cls=<class object path> }, ... }
local ITEMS = %(items)s

local function log(m) print("[PakRatInjector] " .. tostring(m) .. "\\n") end
local function valid(o) return o and o:IsValid() end
local function findObj(p) local ok,o = pcall(StaticFindObject, p); if ok then return o end return nil end

-- Resolve (loading if needed) every class up front; cache the UClass objects.
local RESOLVED = {}   -- category -> { classObj, ... }
local function resolveAll()
    for cat, list in pairs(ITEMS) do
        RESOLVED[cat] = RESOLVED[cat] or {}
        for _, it in ipairs(list) do
            local c = findObj(it.cls)
            if not valid(c) then pcall(LoadAsset, it.pkg); c = findObj(it.cls) end
            if valid(c) then
                RESOLVED[cat][#RESOLVED[cat] + 1] = c
                log("resolved " .. it.cls)
            else
                log("FAILED to load " .. it.cls)
            end
        end
    end
end

local function makeAppender(cat)
    return function(Context, ReturnValue)
        pcall(function()
            if ReturnValue == nil then return end
            local arr = ReturnValue:get(); if arr == nil then return end
            local list = RESOLVED[cat]; if not list then return end
            for _, c in ipairs(list) do
                if valid(c) then arr[arr:GetArrayNum() + 1] = c end
            end
        end)
    end
end

local hooked = false
local function register()
    if hooked then return end
    resolveAll()
    for _, cat in ipairs({ %(cats)s }) do
        if ITEMS[cat] then
            local fn = WIDGET_CLASS .. ":Return Catalogue " .. cat .. " product class"
            local ok = pcall(function() RegisterHook(fn, makeAppender(cat), function() end) end)
            log((ok and "hooked " or "hook FAILED ") .. fn)
        end
    end
    hooked = true
end

-- The widget class isn't loaded at startup; register when it first appears.
NotifyOnNewObject(WIDGET_CLASS, function() register() end)
log("loaded")
"""


def _lua_table(items_by_cat: dict[str, list[dict]]) -> str:
    """Render the ITEMS Lua table literal."""
    lines = ["{"]
    for cat, lst in items_by_cat.items():
        lines.append(f'  ["{cat}"] = {{')
        for it in lst:
            lines.append(f'    {{ pkg="{it["pkg"]}", cls="{it["cls"]}" }},')
        lines.append("  },")
    lines.append("}")
    return "\n".join(lines)


def write_injector_mod(items_by_cat: dict[str, list[dict]],
                       mods_dir: Path | None = None) -> Path:
    """Write the UE4SS Lua mod that registers every item. Returns the mod dir."""
    mods_dir = mods_dir or ue4ss_mods_dir()
    if not mods_dir:
        raise RuntimeError("UE4SS Mods folder not found — is UE4SS installed?")
    mod = mods_dir / MOD_NAME
    (mod / "Scripts").mkdir(parents=True, exist_ok=True)
    (mod / "enabled.txt").write_text("", encoding="utf-8")

    cats = ", ".join(f'"{c}"' for c in items_by_cat)
    lua = _LUA_HEADER % {
        "widget": WIDGET_CLASS,
        "items": _lua_table(items_by_cat),
        "cats": cats,
    }
    (mod / "Scripts" / "main.lua").write_text(lua, encoding="utf-8")

    # Ensure the mod is listed+enabled in mods.txt (UE4SS load list).
    _ensure_mods_txt(mods_dir)
    return mod


def _ensure_mods_txt(mods_dir: Path) -> None:
    txt = mods_dir / "mods.txt"
    line = f"{MOD_NAME} : 1"
    try:
        existing = txt.read_text(encoding="utf-8") if txt.is_file() else ""
    except Exception:
        existing = ""
    if MOD_NAME in existing:
        return
    sep = "" if existing.endswith("\n") or not existing else "\n"
    with txt.open("a", encoding="utf-8") as f:
        f.write(sep + line + "\n")


# ---------------------------------------------------------------------------
# INI manifest — the registry of record for everything Pak Rat has added.
# ---------------------------------------------------------------------------
def manifest_path() -> Path:
    return cook.home() / "additions.ini"


def load_manifest() -> configparser.ConfigParser:
    cp = configparser.ConfigParser()
    p = manifest_path()
    if p.is_file():
        cp.read(p, encoding="utf-8")
    return cp


def manifest_items() -> dict[str, list[dict]]:
    """Read the manifest into {category: [{pkg, cls, name}, ...]}."""
    cp = load_manifest()
    out: dict[str, list[dict]] = {}
    for sec in cp.sections():
        if not sec.startswith("item:"):
            continue
        d = cp[sec]
        cat = d.get("category", "Decoration")
        out.setdefault(cat, []).append({
            "pkg": d.get("pkg", ""), "cls": d.get("cls", ""),
            "name": d.get("name", sec[5:]),
        })
    return out


def save_manifest(items_by_cat: dict[str, list[dict]]) -> Path:
    cp = configparser.ConfigParser()
    cp["pakrat"] = {
        "version": "3.0.0-beta5",
        "updated": datetime.datetime.now().isoformat(timespec="seconds"),
    }
    for cat, lst in items_by_cat.items():
        for it in lst:
            key = "item:" + it.get("name", it["cls"].rsplit(".", 1)[-1])
            cp[key] = {
                "category": cat, "name": it.get("name", ""),
                "pkg": it["pkg"], "cls": it["cls"],
            }
    p = manifest_path()
    with p.open("w", encoding="utf-8") as f:
        cp.write(f)
    return p


def has_additions() -> bool:
    """True if any items were previously added (drives the Page-1 label flip)."""
    return bool(manifest_items())


# ---------------------------------------------------------------------------
# Cook: FBX -> Decoration-derived Blueprint at a NEW /Game path.
#
# EXPERIMENTAL (beta1): the runtime injection mechanism is proven in-game, but
# cooking a brand-new Decoration Blueprint (vs a swap) is new. The cooked BP
# derives from a stub `Decoration` whose parent import resolves to the REAL
# game Decoration class at runtime (same stub-retarget trick v2 uses for
# materials), so it is a genuine TSubclassOf<Decoration>. Product metadata
# (price/name) falls back to the base Decoration defaults for now.
# ---------------------------------------------------------------------------
DECO_ROOT = "/Game/VideoStore/asset/prop/decoration"
DECO_BASE = DECO_ROOT + "/Decoration"          # stub base (retargets at runtime)

_COOK_SCRIPT = r'''
import unreal, traceback
LOG = r"%(log)s"
def note(m):
    try:
        open(LOG, "a").write(str(m) + "\n"); unreal.log("PAKRAT " + str(m))
    except Exception: pass

FBX      = r"%(fbx)s"
NAME     = "%(name)s"
PKG      = "%(pkg)s"                       # /Game/VideoStore/asset/prop/decoration/<Name>
MESH_PKG = PKG
MESH_NM  = "SM_" + NAME
DECO_BASE = "%(deco_base)s"
at  = unreal.AssetToolsHelpers.get_asset_tools()
eal = unreal.EditorAssetLibrary
try:
    # 1) stub Decoration base (Actor-derived); its /Game path is what matters —
    #    runtime links the cooked BP's parent to the real game Decoration.
    if not eal.does_asset_exist(DECO_BASE):
        bf = unreal.BlueprintFactory(); bf.set_editor_property("parent_class", unreal.Actor)
        at.create_asset("Decoration", "%(deco_root)s", unreal.Blueprint, bf)
        eal.save_asset(DECO_BASE, only_if_is_dirty=False)
    deco_gen = unreal.load_asset(DECO_BASE).generated_class()

    # 2) import FBX -> StaticMesh
    opt = unreal.FbxImportUI()
    opt.import_mesh = True; opt.import_as_skeletal = False
    opt.import_materials = False; opt.import_textures = False
    opt.mesh_type_to_import = unreal.FBXImportType.FBXIT_STATIC_MESH
    opt.static_mesh_import_data.combine_meshes = True
    task = unreal.AssetImportTask()
    task.filename = FBX; task.destination_path = MESH_PKG; task.destination_name = MESH_NM
    task.replace_existing = True; task.automated = True; task.save = True; task.options = opt
    at.import_asset_tasks([task])
    mesh = unreal.load_asset(MESH_PKG + "/" + MESH_NM)
    note("MESH " + str(mesh is not None))

    # 3) new Blueprint deriving from Decoration + a StaticMeshComponent root
    bf2 = unreal.BlueprintFactory(); bf2.set_editor_property("parent_class", deco_gen)
    bp = at.create_asset(NAME, PKG, unreal.Blueprint, bf2)
    sds = unreal.get_engine_subsystem(unreal.SubobjectDataSubsystem)
    roots = sds.k2_gather_subobject_data_for_blueprint(bp)
    params = unreal.AddNewSubobjectParams(parent_handle=roots[0],
             new_class=unreal.StaticMeshComponent, blueprint_context=bp)
    handle, fail = sds.add_new_subobject(params)
    if not fail:
        data = sds.k2_find_subobject_data_from_handle(handle)
        comp = unreal.SubobjectDataBlueprintFunctionLibrary.get_object(data)
        comp.set_editor_property("static_mesh", mesh)
    unreal.EditorAssetLibrary.save_asset(PKG + "/" + NAME, only_if_is_dirty=False)
    note("BP_DONE " + PKG + "/" + NAME + "." + NAME + "_C")
except Exception:
    note("COOK_ERR\n" + traceback.format_exc())
'''


def cook_decoration(env: "cook.CookEnv", fbx: str, name: str,
                    progress=None) -> dict:
    """Cook `fbx` into a Decoration BP `name`. Returns {pkg, cls, mount_dir}."""
    name = "".join(c for c in name if c.isalnum() or c == "_") or "PakRatItem"
    pkg = f"{DECO_ROOT}/{name}"
    log = cook.home() / "inject_cook.log"
    try:
        log.unlink()
    except Exception:
        pass
    script = env_script_path()
    script.write_text(_COOK_SCRIPT % {
        "log": str(log), "fbx": fbx, "name": name, "pkg": pkg,
        "deco_base": DECO_BASE, "deco_root": DECO_ROOT,
    }, encoding="utf-8")
    if progress:
        progress(f"Importing + building Blueprint '{name}'…", None)
    cook._ue(env, f"-ExecutePythonScript={script}")
    if progress:
        progress("Cooking…", None)
    cook._ue(env, "-run=cook", "-targetplatform=Windows", "-unversioned", "-cookall")
    return {"pkg_game": pkg, "name": name,
            "cls": f"{pkg}.{name}_C",
            "mount_rel": pkg.replace("/Game/", "RetroRewind/Content/")}


def env_script_path() -> Path:
    return cook.project_dir() / "pakrat_deco_import.py"


def _stage_cooked(name: str, mount_rel_dir: str, stage: Path) -> int:
    """Copy the item's cooked assets into the stage tree at their real mount path.

    The cooker's on-disk layout varies (platform subfolder, <Project>/Content mount
    name), so — like cook.cook_meshes — we don't assume a fixed path: we locate the
    cooked BP `<name>.uasset` anywhere under the cooked tree and stage its whole
    containing folder (BP + its SM_<name> mesh + any .ubulk). Returns files staged.
    """
    cooked_root = cook.project_dir() / "Saved" / "Cooked"
    matches = sorted(cooked_root.rglob(name + ".uasset")) if cooked_root.is_dir() else []
    if not matches:
        return 0
    src = matches[0].parent                       # the item's cooked folder
    dest = stage / Path(*mount_rel_dir.split("/"))
    dest.mkdir(parents=True, exist_ok=True)
    n = 0
    for f in src.iterdir():
        if f.suffix in (".uasset", ".uexp", ".ubulk"):
            shutil.copy2(f, dest / f.name); n += 1
    return n


def run_add_pipeline(items: list[dict], progress=None) -> dict:
    """
    items: [{'fbx': path, 'name': str, 'category': 'Decoration'}, …]
    Cooks each into a Decoration BP, packs them into ONE pak, (re)generates the
    UE4SS injector mod + INI manifest (additive across runs). Returns
    {'pak': path, 'mod': dir, 'ini': path, 'items_by_cat': {...}}.
    """
    import tempfile

    def say(m, p=None):
        if progress:
            progress(m, p)

    if not ue4ss_available():
        raise RuntimeError("UE4SS is not installed — Add Asset needs it for the "
                           "runtime catalogue hook.")
    env = cook.setup(progress=progress)

    cooked = []
    for it in items:
        c = cook_decoration(env, it["fbx"], it["name"], progress=progress)
        c["category"] = it.get("category", "Decoration")
        cooked.append(c)

    # Pack all cooked new assets into one pak.
    say("Packaging .pak…", None)
    work = Path(tempfile.mkdtemp(prefix="pakrat_add_"))
    stage = work / "stage"
    stage.mkdir(parents=True, exist_ok=True)      # repak needs the dir to exist
    staged = 0
    for c in cooked:
        staged += _stage_cooked(c["name"], c["mount_rel"], stage)
    if staged == 0:
        raise RuntimeError(
            "Cook produced no assets to package — nothing was found under the "
            "cooked output for: " + ", ".join(c["name"] for c in cooked) + ".\n"
            "The UE cook step likely failed; see inject_cook.log and the cook logs "
            "in the Pak Rat home folder.")
    label = cooked[0]["name"] + (f"_plus{len(cooked)-1}" if len(cooked) > 1 else "")
    out_pak = work / f"zzz_PakRat_Add_{label}_P.pak"
    core._repak("pack", "--version", core.PAK_VERSION, "--mount-point", core.PAK_MOUNT,
                "--path-hash-seed", core.PAK_SEED, str(stage), str(out_pak))

    # Merge new items into the manifest (additive), then regenerate Lua+INI.
    say("Registering with UE4SS injector…", None)
    items_by_cat = manifest_items()
    for c in cooked:
        items_by_cat.setdefault(c["category"], []).append(
            {"pkg": c["pkg_game"], "cls": c["cls"], "name": c["name"]})
    ini = save_manifest(items_by_cat)
    mod = write_injector_mod(items_by_cat)
    say("Done.", 100)
    return {"pak": str(out_pak), "mod": str(mod), "ini": str(ini),
            "items_by_cat": items_by_cat}
